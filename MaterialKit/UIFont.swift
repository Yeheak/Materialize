import UIKit

extension UIFont {
    class func robotoLightWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Light", size: size)!
    }
    
    class func robotoMediumWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Medium", size: size)!
    }

    class func robotoRegularWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Regular", size: size)!
    }
}
